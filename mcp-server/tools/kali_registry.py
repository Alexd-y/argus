def get_tools_by_category() -> dict[str, list[ToolDefinition]]:
    """Group tools by category."""
    by_cat: dict[str, list[ToolDefinition]] = {}
    for t in KALI_TOOL_REGISTRY:
        by_cat.setdefault(t.category, []).append(t)
    return by_cat


def get_tool_by_name(name: str) -> ToolDefinition | None:
    """Get tool definition by name."""
    for t in KALI_TOOL_REGISTRY:
        if t.name == name:
            return t
    return None

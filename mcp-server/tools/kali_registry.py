"""Kali pentest tool registry — 150+ tool definitions for MCP.

Each tool maps to backend API: POST /api/v1/tools/{name} or /execute.
Excludes exploit/brute-only tools per MCP-002.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from typing import Any


@dataclass
class ArgSchema:
    """Argument schema for a tool."""

    name: str
    description: str
    required: bool = True
    default: Any = None
    type: str = "string"


@dataclass
class ToolDefinition:
    """Definition of a Kali pentest tool for MCP registration."""

    name: str
    description: str
    category: str
    backend_endpoint: str  # "nmap" | "execute" | tool-specific
    args_schema: list[ArgSchema] = field(default_factory=list)


def _arg(name: str, desc: str, required: bool = True, default: Any = None, type: str = "string") -> ArgSchema:
    return ArgSchema(name=name, description=desc, required=required, default=default, type=type)


